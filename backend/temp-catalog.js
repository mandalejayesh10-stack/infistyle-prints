// Helper to look up a subproduct by slug
export function getProductBySlug(slug: string): { product: SubProduct; category: Category } | null {
  for (const cat of PRODUCT_CATALOG) {
    const item = cat.items.find(i => i.slug === slug);
    if (item) return { product: item, category: cat };
  }
  return null;
}
